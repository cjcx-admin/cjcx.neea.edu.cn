subject.buildSubjectList = function(isResults) {
    var latestSubjectList = isResults ? subject.LATEST_RESULTS_LIST : subject.LATEST_CERT_LIST,
        normalSubjectList = isResults ? subject.NORMAL_RESULTS_LIST : subject.NORMAL_CERT_LIST;
    //“最新查询”模块
    var div = "";
    if (latestSubjectList.length > 0) {
        div += '<div class="subject_container_title">最新查询</div>';
        div += '<div class="subject_container_box">';
        div += this.buildSubjectUL(latestSubjectList);
        div += "</div>";
    }
    //“选择考试项目”模块
    if (normalSubjectList.length > 0) {
        div += '<div class="subject_container_title">选择考试项目</div>';
        div += '<div class="subject_container_box">';
        div += this.buildSubjectUL(normalSubjectList);
        div += '</div>';
        div += '<div class="subject_container_stop_tip">注：灰色字体为已停考项目</div>';
    }
    document.getElementById("subject_container").innerHTML = div;
};
subject.buildSubjectUL = function(subjectList) {
    var ul = "";
    if (subjectList.length > 0) {
        ul += '<ul>';
        $.each(subjectList, function(index, bean) {
            var code = bean["CODE"];
            var name = bean["NAME"];
            var url = bean["URL"];
            var stop = bean["STOP"] || 0;
            ul += '<li ' + (stop ? 'class="stop"' : '') + '>';
            ul += '<a href="' + url + '" target="_blank" title="' + name + '" icode="' + code + '">' + name + '</a>';
            ul += '</li>';
        });
        ul += '</ul>';
    }
    return ul;
};
