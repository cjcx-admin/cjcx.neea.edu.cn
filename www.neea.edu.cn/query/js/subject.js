subject.getSubjectByCode = function(code, isResults) {
    var subjectList = isResults ? subject.NORMAL_RESULTS_LIST : subject.NORMAL_CERT_LIST;
    var subjectJO = null;
    if (code) {
        code = code.toUpperCase();
        for (let i = 0; i < subjectList.length; i++) {
            var item = subjectList[i];
            if (item["CODE"] == code) {
                subjectJO = item;
                break;
            }
        }
    }
    return subjectJO;
};